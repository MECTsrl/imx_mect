#ifndef HEADER_DH_H
#include <openssl/dh.h>
#endif
DH *get_dh512()
	{
	static unsigned char dh512_p[]={
		0xB2,0x5C,0x01,0x6F,0xDD,0xD2,0x6B,0x42,0x9B,0x1D,0x10,0xDA,
		0x8E,0xA8,0xC6,0x2A,0x87,0x78,0x35,0x68,0xF1,0x94,0xDC,0x08,
		0xB4,0x62,0xA9,0xB9,0x60,0x9C,0xAA,0x7A,0xF3,0xC0,0x8E,0x2D,
		0x3D,0xF7,0xA8,0x4E,0xCA,0x79,0x0F,0x60,0xB7,0x34,0xB6,0x71,
		0x8D,0x0D,0xBE,0xC1,0x3C,0xC8,0x71,0xCE,0x2C,0x0F,0xB3,0x01,
		0x71,0xBB,0xDA,0x9B,
		};
	static unsigned char dh512_g[]={
		0x02,
		};
	DH *dh;

	if ((dh=DH_new()) == NULL) return(NULL);
	dh->p=BN_bin2bn(dh512_p,sizeof(dh512_p),NULL);
	dh->g=BN_bin2bn(dh512_g,sizeof(dh512_g),NULL);
	if ((dh->p == NULL) || (dh->g == NULL))
		{ DH_free(dh); return(NULL); }
	return(dh);
	}
